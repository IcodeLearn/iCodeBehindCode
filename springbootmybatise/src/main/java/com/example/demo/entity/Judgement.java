package com.example.demo.entity;

public class Judgement {
    private String judgementId;

    private String judgementText;

    private String judgementA;

    private String judgementB;

    private String judgementAnswer;

    private String judgementLevel;

    public String getJudgementId() {
        return judgementId;
    }

    public void setJudgementId(String judgementId) {
        this.judgementId = judgementId;
    }

    public String getJudgementText() {
        return judgementText;
    }

    public void setJudgementText(String judgementText) {
        this.judgementText = judgementText;
    }

    public String getJudgementA() {
        return judgementA;
    }

    public void setJudgementA(String judgementA) {
        this.judgementA = judgementA;
    }

    public String getJudgementB() {
        return judgementB;
    }

    public void setJudgementB(String judgementB) {
        this.judgementB = judgementB;
    }

    public String getJudgementAnswer() {
        return judgementAnswer;
    }

    public void setJudgementAnswer(String judgementAnswer) {
        this.judgementAnswer = judgementAnswer;
    }

    public String getJudgementLevel() {
        return judgementLevel;
    }

    public void setJudgementLevel(String judgementLevel) {
        this.judgementLevel = judgementLevel;
    }
}
